/**
 * Detect city from a Mexican phone number.
 * Supports formats: 521XXXXXXXXXX, 52XXXXXXXXXX, XXXXXXXXXX
 */
export function detectCity(phone: string): string | null {
  // Strip non-digits
  const digits = phone.replace(/\D/g, '');

  // Remove country code prefix (52) and optional 1
  let local = digits;
  if (local.startsWith('521')) local = local.slice(3);
  else if (local.startsWith('52')) local = local.slice(2);

  if (local.length < 7) return null;

  // Try 3-digit area code first (more specific)
  const area3 = local.slice(0, 3);
  if (AREA_CODES[area3]) return AREA_CODES[area3];

  // Try 2-digit area code
  const area2 = local.slice(0, 2);
  if (AREA_CODES[area2]) return AREA_CODES[area2];

  return null;
}

/**
 * Get a short city label for display
 */
export function getCityShort(phone: string): string {
  return detectCity(phone) || 'Desconocida';
}
